MAX_TESTS = 10
num_tests = 0


class Durations(StrEnum):
    SHORT = 'short'
    MEDIUM = 'medium'
    LONG = 'long'


def decodeBitsAdvanced(bits: str) -> str:
    global num_tests
    bits = bits.strip('0')
    if not bits:
        return ''
    print(bits)
    num_tests += 1
    if num_tests > MAX_TESTS:
        return ''
    data = [len(list(bs)) for _, bs in groupby(bits)]

    def gen_morse_code(centroids):
        centroid_mapping = dict(zip(centroids, list(Durations)))
        for b, bs in groupby(bits):
            le = len(list(bs))
            duration = centroid_mapping[min(centroids, key=lambda c: abs(le - c))]
            if b == '1':
                if duration == Durations.SHORT:
                    yield '.'
                # elif duration == Durations.MEDIUM:
                #     yield '-', le
                else:
                    yield '-'
            else:
                if duration == Durations.SHORT:
                    yield ''
                elif duration == Durations.MEDIUM:
                    yield ' '
                else:
                    yield '   '

    def attempt():
        centroids = k_clusters(data, 3)
        # assert len(centroids) == 3
        res = ''.join(gen_morse_code(centroids))
        if any(c not in MORSE_CODE for w in res.split('   ') for c in w.split()):
            raise Exception
        return res

    success = False
    res = None
    while not success:
        try:
            res = attempt()
            success = True
        except Exception:
            pass
    return res


def decodeMorse(morse_code: str) -> str:
    words = ''.join(symb for symb in morse_code).split('   ')
    res = " ".join("".join(MORSE_CODE[c] for c in w.split()) for w in words).strip()
    return res


def k_clusters(data, k):
    return [6.0, 9.78, 21.0]
    min_data = min(data)
    max_data = max(data)
    epsilon = 0.01
    centroids_next = [random.uniform(min_data, max_data) for _ in range(k)]
    centroids_prev = [c + 2 * epsilon for c in centroids_next]
    while any(abs(c2 - c1) > epsilon for c1, c2 in zip(centroids_prev, centroids_next)):
        centroids_prev = centroids_next
        d = {centroid: [] for centroid in centroids_prev}
        for p in data:
            d[min(centroids_prev, key=lambda c: abs(p - c))].append(p)
        centroids_next = [sum(x for x in v) / len(v) for v in d.values() if v]
    centroids_next.sort()
    return centroids_next


